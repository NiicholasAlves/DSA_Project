    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        // TODO add your handling code here:
        String text = jTextArea3.getText();
        String[] lines = text.split("\\r?\\n");
        String lastLine = lines[lines.length - 1];
        
        StringBuilder modifiedText = new StringBuilder();
        for (int i = 0; i < lines.length - 1; i++) {
            modifiedText.append(lines[i]).append("\n");
        }
        
        modifiedText.insert(0, lastLine + "\n");
        jTextArea3.setText(modifiedText.toString());
    }//GEN-LAST:event_jButton11ActionPerformed

    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // TODO add your handling code here:
        String text = jTextArea3.getText();
        
        // Split the text into lines
        String[] lines = text.split("\\r?\\n");
        
        // Remove the first line and store it in a variable
        String firstLine = lines[0];
        
        // Remove the first line from the original text
        StringBuilder modifiedText = new StringBuilder();
        for (int i = 1; i < lines.length; i++) {
            modifiedText.append(lines[i]).append("\n");
        }
        
        // Append the removed line at the end of the modified text
        modifiedText.append(firstLine).append("\n");
        
        // Set the modified text back to the text area
        jTextArea3.setText(modifiedText.toString());
    }//GEN-LAST:event_jButton12ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String searchQuery = jTextField5.getText().trim();
        
        String likedSongsText = jTextArea3.getText();
        
        String[] lines = likedSongsText.split("\\r?\\n");
        
        boolean songFound = false;
        
        for (String line : lines) {
            
            if (line.contains(searchQuery)) {
                
                jTextArea3.append(line + "\n");
                
                
                songFound = true;
            }
        }
        
        if (!songFound) {
            JOptionPane.showMessageDialog(null, "Song not found.", "Search Result", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

