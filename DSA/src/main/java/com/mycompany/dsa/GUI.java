    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String likedSongs = jTextArea1.getText();
        
        String[] lines = likedSongs.split("\\r?\\n");
        
        String lastSong = lines[lines.length - 1];
        
        String[] songDetails = lastSong.split(", ");
        String lastSongGenre = songDetails[songDetails.length - 1].replace("Genre: ", "");
        String lastSongArtist = songDetails[0].replace("Artist: ", "");
        String lastSongTitle = songDetails[1].replace("Title: ", "");
        
        if (lastSongGenre.equals("Classic") || lastSongGenre.equals("Rock") || lastSongGenre.equals("Hip-hop")) {
            jTextArea2.append("Artist: " + lastSongArtist + ", Title: " + lastSongTitle + ", Genre: " + lastSongGenre + "\n");
        } else if (lastSongGenre.equals("HipHop") || lastSongGenre.equals("Classical")) {
            jTextArea3.append("Artist: " + lastSongArtist + ", Title: " + lastSongTitle + ", Genre: " + lastSongGenre + "\n");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

