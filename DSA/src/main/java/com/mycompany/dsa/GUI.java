    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String searchQuery = jTextField4.getText().trim();
        
        String likedSongsText = jTextArea2.getText();
        
        String[] lines = likedSongsText.split("\\r?\\n");
        
        StringBuilder foundSongText = new StringBuilder();
       
        boolean songFound = false;
        
        for(String line: lines){
            if(line.contains(searchQuery)){
                jTextArea2.setText(line);
                songFound = true;
                break;
            }
        }
        
        // If no song matching the search query is found, display a message
        if (!songFound) {
            JOptionPane.showMessageDialog(null, "No matching song found.", "Search Result", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:
        String text = jTextArea2.getText();
        
        // Split the text into lines
        String[] lines = text.split("\\r?\\n");
        
        // Remove the first line and store it in a variable
        String firstLine = lines[0];
        
        // Remove the first line from the original text
        StringBuilder modifiedText = new StringBuilder();
        for (int i = 1; i < lines.length; i++) {
            modifiedText.append(lines[i]).append("\n");
        }
        
        // Append the removed line at the end of the modified text
        modifiedText.append(firstLine).append("\n");
        
        // Set the modified text back to the text area
        jTextArea2.setText(modifiedText.toString());
    }//GEN-LAST:event_jButton8ActionPerformed

